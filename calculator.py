#BUILDING A BETTER CALCULATOR

num1 = float(input("Enter a first number: "))
op = input("Enter a opeartor: ")
num2 = float(input("Enter a second number: "))

if op == "+":
    print(num1 + num2)

elif op == "-":
    print(num1 - num2)

elif op == "/":
    print(num1 / num2)

elif op == "*":
    print(num1 * num2)

else :
    print("Invalid operator")
